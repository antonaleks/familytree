function Section({ title, items }) {
  return (
    <div className="ft-help-sec">
      <h4>{title}</h4>
      <dl className="ft-help-dl">
        {items.map(([t, d]) => (
          <div key={t} className="ft-help-item">
            <dt>{t}</dt><dd>{d}</dd>
          </div>
        ))}
      </dl>
    </div>
  );
}

export default function HelpModal({ onClose }) {
  return (
    <div className="ft-overlay" onClick={e => {
      if (e.target.classList.contains('ft-overlay')) onClose();
    }}>
      <div className="ft-modal ft-modal-help">
        <button className="ft-close" onClick={onClose}>×</button>
        <h3>Как пользоваться</h3>
        <Section title="Просмотр" items={VIEW} />
        <Section title="Редактирование" items={EDIT} />
      </div>
    </div>
  );
}
